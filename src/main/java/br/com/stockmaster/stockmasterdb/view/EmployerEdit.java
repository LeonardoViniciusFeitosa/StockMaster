    private void registerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerButtonActionPerformed
// Pega os valores dos campos
    String name = nameField.getText().trim();
    String doc = docField.getText().replaceAll("[^0-9]", ""); // só números
    String address = addressField.getText().trim();
    String number = numberField.getText().replaceAll("[^0-9]", ""); // só números
    String email = emailField.getText().trim();
    String role = roleField.getText().trim();

    // Verifica se campos obrigatórios estão preenchidos
    if (name.isEmpty() || doc.isEmpty() || address.isEmpty() || number.isEmpty() 
        || email.isEmpty() || role.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Preencha todos os campos corretamente!");
        return;
    }

    // Validação CPF (11 dígitos)
    if (!doc.matches("\\d{11}")) {
        JOptionPane.showMessageDialog(this, "CPF inválido! Deve conter 11 dígitos.");
        return;
    }

    // Validação telefone (10 ou 11 dígitos)
    if (!number.matches("\\d{10,11}")) {
        JOptionPane.showMessageDialog(this, "Número de telefone inválido! Deve conter 10 ou 11 dígitos.");
        return;
    }

    // Validação email simples
    if (!email.matches("^[\\w.-]+@[\\w.-]+\\.[a-zA-Z]{2,6}$")) {
        JOptionPane.showMessageDialog(this, "Email inválido! Informe um email no formato correto. (EX: email@gmail.com)");
        return;
    }

    // Busca funcionário pelo ID
    Employer employer = TempDatabase.getEmployerById(EmployerId);
    
    if (employer == null) {
        JOptionPane.showMessageDialog(this, "Funcionário não encontrado.");
        return;
    }

    // Atualiza dados do funcionário
    employer.setName(name);
    employer.setPersonalId(doc);
    employer.setAddress(address);
    employer.setNumber(number);
    employer.setEmail(email);
    employer.setRole(role);

    JOptionPane.showMessageDialog(this, "Funcionário alterado com sucesso!");
    }//GEN-LAST:event_registerButtonActionPerformed

