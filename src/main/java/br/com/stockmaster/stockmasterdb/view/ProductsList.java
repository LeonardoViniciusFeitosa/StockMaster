    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
int selectedRow = productTable.getSelectedRow();
if (selectedRow == -1){
JOptionPane.showMessageDialog(rootPane, "Selecione um produto para editar.");
return;
}
int id = (int) productTable.getValueAt(selectedRow, 0);

ProductsEdit edit = new ProductsEdit(id);
edit.setVisible(true);
this.dispose();
    }//GEN-LAST:event_editButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
 int selectedRow = productTable.getSelectedRow();
 if (selectedRow == -1){
 JOptionPane.showMessageDialog(rootPane, "Selecione um produto para excluir.");
 return;
 }
 String name = (String) productTable.getValueAt(selectedRow, 1);
 int id = (int) productTable.getValueAt(selectedRow, 0);
 int r = JOptionPane.showConfirmDialog(rootPane, "Tem certeza que deseja apagar " + name + "?", "Confirmação", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
 if (r == JOptionPane.YES_OPTION){
     
new ProductDAO().delete(id);
listUpdate();
 
 listUpdate();
 }
    }//GEN-LAST:event_deleteButtonActionPerformed

